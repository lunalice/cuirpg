package cuirpg.Items;

public class ProtectiveGear {
	private String name;
	private int defencePower;

	public ProtectiveGear(String name, int defencePower) {
		this.name = name;
		this.defencePower = defencePower;
	}

	public String getName() {
		return name;
	}

	public int getDefencePower() {
		return defencePower;
	}

}
